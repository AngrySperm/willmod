library willmod;

import 'package:flutter/cupertino.dart';

class WillMod {
  static init(
      {Color? colorPrimary,
      Color? colorPrimaryDark,
      Color? colorPrimaryLightest,
      Color? colorPrimaryLighter}) {
    /*color*/
    if (colorPrimary != null) {
      colorPrimary = colorPrimary;
    }
    if (colorPrimaryDark != null) {
      colorPrimaryDark = colorPrimaryDark;
    }
    if (colorPrimaryLightest != null) {
      colorPrimaryLightest = colorPrimaryLightest;
    }
    if (colorPrimaryLighter != null) {
      colorPrimaryLighter = colorPrimaryLighter;
    }
  }
}
